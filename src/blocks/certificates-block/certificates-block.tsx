import React from 'react';
import styles from './certificates-block.module.scss';
import { BlockLayout } from '../../components/block-layout/block-layout';
import { Swiper, SwiperSlide, useSwiper } from 'swiper/react';
import 'swiper/css';
import { certificatesImages } from '../../autogenerated/certificates-images';
import cn from 'classnames';

export const CertificatesBlock = () => {
    return (
        <BlockLayout title="Сертификаты и партнеры" id="certificates" paddingBottom paddingTop>
            <Swiper
                spaceBetween={16}
                slidesPerView={1}
                onSlideChange={() => console.log('slide change')}
                onSwiper={(swiper) => console.log(swiper)}
                className={styles.swiper}
                autoHeight
                loop
                autoplay={{ delay: 100 }}
                breakpoints={{
                    1200: {
                        slidesPerView: 4,
                    },
                    960: {
                        spaceBetween: 32,
                        slidesPerView: 3,
                    },
                    600: {
                        slidesPerView: 2,
                    },
                }}
            >
                {certificatesImages.map((item, i) => {
                    return (
                        <SwiperSlide key={i}>
                            <div className={styles.imageWrap}>
                                <img src={item} alt="" />
                            </div>
                        </SwiperSlide>
                    );
                })}
                <Navigation />
            </Swiper>
        </BlockLayout>
    );
};

const Navigation = () => {
    const swiper = useSwiper();

    const prev = () => {
        swiper.slidePrev();
    };
    const next = () => {
        swiper.slideNext();
    };

    return (
        <div className={styles.navigation}>
            <button onClick={prev} className={styles.navigationButton}>
                <span className={cn('material-symbols-outlined', styles.icon)}>arrow_back_ios_new</span>
            </button>

            <button onClick={next} className={styles.navigationButton}>
                <span className={cn('material-symbols-outlined', styles.icon)}>arrow_forward_ios</span>
            </button>
        </div>
    );
};
